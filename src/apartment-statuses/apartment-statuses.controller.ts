import { Controller, Get, Post, Body, Patch, Param, Delete, UseGuards } from '@nestjs/common';
import { JwtAuthGuard } from '../common/guards/jwt-auth.guard';
import { AdminGuard } from '../common/guards/admin.guard';
import { ApartmentStatusesService } from './apartment-statuses.service';
import { CreateApartmentStatusDto } from './dto/create-apartment-status.dto';
import { UpdateApartmentStatusDto } from './dto/update-apartment-status.dto';

@UseGuards(JwtAuthGuard)
@Controller('apartment-statuses')
export class ApartmentStatusesController {
  constructor(private readonly service: ApartmentStatusesService) {}

  @Get()
  findAll() {
    return this.service.findAll();
  }

  @Get(':id')
  findOne(@Param('id') id: string) {
    return this.service.findOne(id);
  }

  @Post()
  @UseGuards(AdminGuard)
  create(@Body() dto: CreateApartmentStatusDto) {
    return this.service.create(dto);
  }

  @Patch(':id')
  @UseGuards(AdminGuard)
  update(@Param('id') id: string, @Body() dto: UpdateApartmentStatusDto) {
    return this.service.update(id, dto);
  }

  @Delete(':id')
  @UseGuards(AdminGuard)
  remove(@Param('id') id: string) {
    return this.service.remove(id);
  }
}
